#include "../arraymodule.hpp"
#include <iostream>

using namespace std;

/// @brief reading array from console
/// @param array source array
/// @param size size of array
void read(int array[], int size) {
    for (int i = 0; i < size; i++) {
        cin >> array[i];
    }
}

